#include "../incs/computor.h"

int			operation_add(t_terme **a, t_terme **b)
{
	if (!a || !*a || !b || !*b)
		return (0);
	(*a)->a = (*a)->a + (*b)->a;
	(*b)->a = 0;
	return (0);
}
